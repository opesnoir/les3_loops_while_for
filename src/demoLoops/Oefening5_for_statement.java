package demoLoops;

// for statements
public class Oefening5_for_statement {
    public static void main(String[] args) {
        //shortkey: fori > tab, daarna tab je om hem te voorzien van jou persoonlijke vereiste, zoals bijvoorbeeld de conditie. Bij een conditie van 5 wordt dan gekeken is i nog kleiner dan 5, zo ja dan herhalen we de functie, zo nee dan stoppen we met de functie te herhalen.
        for (int i = 0; i < 5; i++) {
            //dit is de body, hier geef je aan wat deze functie moet doen
            System.out.println("hoera!");
        }
    }
}
